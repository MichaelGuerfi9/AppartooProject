<?php

namespace AdressBookBundle\Entity;

/**
 * Contacts
 */
class Contacts
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var int
     */
    public $user;

    public $contact;



    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }




    /**
     * Set user
     *
     * @param \AdressBookBundle\Entity\User $user
     *
     * @return Contacts
     */
    public function setUser(\AdressBookBundle\Entity\User $user = null)
    {
        $this->user = $user;

        return $this;
    }

    /**
     * Get user
     *
     * @return \AdressBookBundle\Entity\User
     */
    public function getUser()
    {
        return $this->user;
    }

    /**
     * Set contact
     *
     * @param \AdressBookBundle\Entity\User $contact
     *
     * @return Contacts
     */
    public function setContact(\AdressBookBundle\Entity\User $contact = null)
    {
        $this->contact = $contact;

        return $this;
    }

    /**
     * Get contact
     *
     * @return \AdressBookBundle\Entity\User
     */
    public function getContact()
    {
        return $this->contact;
    }
}
